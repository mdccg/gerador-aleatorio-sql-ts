INSERT INTO ordem_servico
	SELECT
        codigo,
        data_emissao,
        item,
        cliente_codigo,
        servico_codigo
    FROM
        (SELECT
            ROUND(RANDOM())::INTEGER::BOOLEAN AS processar_tupla,
            NEXTVAL('ordem_servico_codigo_seq') AS codigo,
            NOW() - '1 year'::INTERVAL * RANDOM() * 5 AS data_emissao,
            CASE servico_codigo
                WHEN 1 THEN ( -- Manicure
                    CASE ROUND(RANDOM() * 2) 
                        WHEN 0 THEN 'Corte'
                        WHEN 1 THEN 'Pintura'
                        WHEN 2 THEN 'Acrílico'
                    END
                )
                WHEN 2 THEN ( -- Pedicure
                    CASE ROUND(RANDOM() * 2)
                        WHEN 0 THEN 'Corte'
                        WHEN 1 THEN 'Pintura'
                        WHEN 2 THEN 'Acrílico'
                    END
                )
                WHEN 3 THEN ( -- Noivado
                    CASE ROUND(RANDOM() * 9) 
                        WHEN 0 THEN 'Manicure'
                        WHEN 1 THEN 'Pedicure'
                        WHEN 2 THEN 'Alongamento'
                        WHEN 3 THEN 'Assistência ao vestir'
                        WHEN 4 THEN 'Cabeleireiro'
                        WHEN 5 THEN 'Spa'
                        WHEN 6 THEN 'Sapatilha'
                        WHEN 7 THEN 'Perfume'
                        WHEN 8 THEN 'Álbum'
                        WHEN 9 THEN 'Remoção da maquiagem'
                    END
                )
                WHEN 4 THEN ( -- Esteticista
                    CASE ROUND(RANDOM() * 5) 
                        WHEN 0 THEN 'Piercing'
                        WHEN 1 THEN 'Botox'
                        WHEN 2 THEN 'Pele'
                        WHEN 3 THEN 'Hidratação'
                        WHEN 4 THEN 'Massagem'
                        WHEN 5 THEN 'Limpeza de pele'
                    END
                )
                WHEN 5 THEN ( -- Cabeleireiro
                    CASE ROUND(RANDOM() * 5)
                        WHEN 0 THEN 'Corte'
                        WHEN 1 THEN 'Chapinha'
                        WHEN 2 THEN 'Navalha'
                        WHEN 3 THEN 'Barba'
                        WHEN 4 THEN 'Alongamento capilar'
                        WHEN 5 THEN 'Café'
                    END
                )
            END item,
            cliente_codigo,
            servico_codigo
        FROM cliente_servico A CROSS JOIN GENERATE_SERIES(1, 9) B) A
    WHERE processar_tupla = true; -- Randomiza a quantidade de ordens de serviço por cliente

