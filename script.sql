SELECT cliente.nome AS noiva, cliente_servico.preco_cliente AS gastos FROM cliente
	INNER JOIN cliente_servico ON cliente.codigo = cliente_servico.cliente_codigo
	INNER JOIN servico ON servico.codigo = cliente_servico.servico_codigo
	WHERE servico LIKE 'Noivado'
	ORDER BY gastos DESC;

