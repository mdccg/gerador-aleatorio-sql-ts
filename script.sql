	INNER JOIN cliente_servico ON cliente.codigo = cliente_servico.cliente_codigo
	INNER JOIN servico ON servico.codigo = cliente_servico.servico_codigo
	WHERE servico LIKE 'Noivado';
	
-- Noiva e seus serviços utilizados
SELECT
	cliente.nome AS noiva,
	servico.servico,
	ordem_servico.item
	FROM cliente
	INNER JOIN cliente_servico ON cliente.codigo = cliente_servico.cliente_codigo
	INNER JOIN servico ON cliente_servico.servico_codigo = servico.codigo
	INNER JOIN ordem_servico ON servico.codigo = ordem_servico.servico_codigo AND cliente.codigo = ordem_servico.cliente_codigo
	WHERE servico LIKE 'Noivado'
	ORDER BY noiva ASC;

-- Itens por serviço
SELECT servico.servico, ordem_servico.item
    FROM servico
    INNER JOIN ordem_servico ON servico.codigo = ordem_servico.servico_codigo
    GROUP BY (servico.servico, ordem_servico.item)
    ORDER BY servico.servico ASC;

