DROP TABLE IF EXISTS cliente, servico, cliente_servico, ordem_servico;

CREATE TABLE IF NOT EXISTS cliente (
	codigo BIGSERIAL PRIMARY KEY,
	nome VARCHAR NOT NULL,
	data_nascimento DATE NOT NULL,
	rua VARCHAR NOT NULL,
	numero VARCHAR NOT NULL,
	bairro VARCHAR NOT NULL,
	cidade VARCHAR NOT NULL
);

CREATE TABLE IF NOT EXISTS servico (
	codigo SERIAL PRIMARY KEY,
	servico VARCHAR NOT NULL UNIQUE,
	preco_custo FLOAT NOT NULL
);

CREATE TABLE IF NOT EXISTS cliente_servico (
	preco_cliente FLOAT NOT NULL,
	cliente_codigo INTEGER REFERENCES cliente (codigo),
	servico_codigo INTEGER REFERENCES servico (codigo),
	PRIMARY KEY(cliente_codigo, servico_codigo)
);

CREATE TABLE IF NOT EXISTS ordem_servico (
	codigo SERIAL PRIMARY KEY,
	data_emissao TIMESTAMP NOT NULL,
	item VARCHAR NOT NULL,
	cliente_codigo INTEGER,
	servico_codigo INTEGER,
	FOREIGN KEY (cliente_codigo, servico_codigo)
		REFERENCES cliente_servico (cliente_codigo, servico_codigo)
);

